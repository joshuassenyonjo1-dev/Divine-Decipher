// Simple random generator helper
export function getRandomLocalWord(difficulty?: 'easy' | 'medium' | 'hard'): BibleWord {
  const filtered = difficulty 
    ? LOCAL_BIBLE_WORDS.filter(w => w.difficulty === difficulty)
    : LOCAL_BIBLE_WORDS;
  
  const pool = filtered.length > 0 ? filtered : LOCAL_BIBLE_WORDS;
  const randomIndex = Math.floor(Math.random() * pool.length);
  return pool[randomIndex];
}
